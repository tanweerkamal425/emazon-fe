<script>
import Pagination from '@/components/Pagination.vue'
import Button from '../../components/flowbite/Button.vue';
import { FwbSelect } from 'flowbite-vue'

export default {
    components: {
        Pagination,
        Button,
        FwbSelect
    },

    props: {
        products: Array,
        categories: Array,
    },

    data() {
        return {
            searchInput: null,
            checkedItems: [],
            modifiedCategories: this.categories,
            selected: {
                categoryId: '',
                price: '',
            },
        }
    },

    methods: {

        onPageChange(pageNum) {
            // console.log(pageNum);
            this.$emit('page-change', pageNum);
        },

        onInput(event) {
            // console.log('ok');
            this.$emit('input', event.target.value);
        },

        onInputCheck(event) {
            this.$emit('checked', event.target.value);
        },

        onSelected(event) {
            if (event.target.id=='price') {
                this.selected.price = event.target.value;
            } 
            if (event.target.id =='category') {
                this.selected.categoryId = event.target.value;
            }
            this.$emit('selected', this.selected);
        },
    },

    emits: ['page-change', 'input', 'checked', 'selected']
}
</script>